package com.company;

public class Boss {
    private int health;
    private int damage;
    private String typeProtect;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getTypeProtect() {
        return typeProtect;
    }

    public void setTypeProtect(String typeProtect) {
        this.typeProtect = typeProtect;
    }
}
